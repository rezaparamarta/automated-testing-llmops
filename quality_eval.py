from langchain_core.prompts import ChatPromptTemplate
from langchain_openai import ChatOpenAI
from langchain_core.output_parsers import StrOutputParser


def create_quality_eval_chain(agent_response):

    system_prompt = """
You are an evaluator that grades the quality of quizzes generated by an AI system.

Your job is to score the quiz from 0 to 10 based on:

1. Format correctness
2. Relevance to the topic
3. Clarity of questions
4. Educational usefulness

Return your result in this format:

Score: <number between 0 and 10>
Explanation: <brief reasoning>
"""

    user_prompt = f"""
Evaluate the following quiz.

Quiz:
{agent_response}

Remember:
Score must be between 0 and 10.
"""

    prompt = ChatPromptTemplate.from_messages([
        ("system", system_prompt),
        ("human", user_prompt),
    ])

    return prompt | ChatOpenAI(
        model="gpt-3.5-turbo",
        temperature=0
    ) | StrOutputParser()